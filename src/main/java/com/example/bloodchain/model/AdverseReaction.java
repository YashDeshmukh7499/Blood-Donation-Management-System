package com.example.bloodchain.model;

/**
 * Enum representing adverse reactions during blood transfusion.
 */
public enum AdverseReaction {
    NONE,           // No adverse reaction
    MILD,           // Mild reaction (itching, rash)
    MODERATE,       // Moderate reaction (fever, chills)
    SEVERE          // Severe reaction (anaphylaxis, requires immediate intervention)
}
